#include <stdio.h>
#include <limits.h>

int		main(void)
{
	/*	==================== ФЛАГИ ==================== */
	printf("\n============= FLAGS \n");
	/* минимальная ширина 2. Ширина числа больше 2, поэтому вывод полного значения */
	printf("  %%2d\t\t=\t'%2d'\n", 123);			//	'123'
	/* минимальная ширина 6. Ширина числа меньше 6, поэтому вывод значения
	** сопровождается пробелами перед числом */
	printf("  %%6d\t\t=\t'%6d'\n", 123);			//	'   123'
	/* флаг -; минимальная ширина 6. Флаг '-' выравнивает значение по левому краю */
	printf("  %%-6d\t\t=\t'%-6d'\n", 123);			//	'123   '
	/* флаг +; минимальная ширина 6. Флаг '+' всегда показывает знак для
	** десятичного числового значения (в том числе знак плюс) */
	printf("  %%+6d\t\t=\t'%+6d'\n", 123);			//	'  +123'
	/* флаг 0; минимальная ширина 6. Если минимальная ширина больше ширины значения
	** флаг '0' дополнит недостоющие места перед значением 0 */
	printf("  %%06d\t\t=\t'%06d'\n", 123);			//	'000123'
	/* флаг пробел. Ставит пробел перед значением если первый символ не знак */
	printf("  %% d\t\t=\t'% d'\n", 123);			//	' 123'
	/* флаг пробел. Здесь есть знак, поэтому нет пробела. Пробел игнорируется */
	printf("  %% d\t\t=\t'% d'\n", -123);			//	'-123'
	/* флаг пробел. Здесь есть знак, поэтому нет пробела. Пробел игнорируется */
	printf("  %%+ d\t\t=\t'%+ d'\n", 123);			//	'+123'

	/* ==================== МОДИФИКАТОР ШИРИНЫ ==================== */
	printf("\n============= WIDTH \n");
	/* Модификатор *; флаг +. Указывает минимальную ширину поля (включая знак для чисел) */
	printf("  %%*d\t\t=\t'%*d'\n", 6, 123);			//	'   123'
	printf("  %%+*d\t\t=\t'%+*d'\n", 6, 123);		//	'  +123'
	/* Модификатор *; флаг +, 0. Поставит плюс и нулю перед числом. Расположение нуля
	** и плюса не имеет значения */
	printf("  %%+0*d\t\t=\t'%+0*d'\n", 6, 123);		//	'+00123'
	printf("  %%0+*d\t\t=\t'%0+*d'\n", 6, 123);		//	'+00123'

	/* ==================== МОДИФИКАТОР РАЗМЕРА ==================== */
	printf("\n============= SIZE \n");



	/* ==================== ПОДВОДНЫЕ КАМНИ ==================== */
	printf("\n============= PITFALLS \n");
	/* При превышении кол-ва аргументов будет выведен мусор */
	printf("  %d, %d, %d, %d\n", 6, 123);		//	'6, 123, trash, trash'
	/* При недостаче аргументов они выведены не будут */
	printf("  %d, %d\n", 6, 123, 10, 43);		//	'6, 123, trash, trash'
	/* Много знаков точности не влияют на исход */
	printf("%.........................5d", 32);	//	'00032'

	printf("\n\n");

	printf("signed char from \t%d to %d\n", SCHAR_MIN, SCHAR_MAX);
	printf("unsigned char from \t0 to %d\n", UCHAR_MAX);
	printf("short from \t\t%d to %d\n", SHRT_MIN, SHRT_MAX);
	printf("signed int from \t%d to %d\n", INT_MIN, INT_MAX);
	printf("unsigned int from \t0 to %u\n", UINT_MAX);
	printf("long int from \t\t%ld to %ld\n", LONG_MIN, LONG_MAX);
	printf("unsigned long from \t0 to %lu\n", ULONG_MAX);
	printf("long long int from \t%lld to %lld\n", LLONG_MIN, LLONG_MAX);
	printf("unsgined long long from 0 to %llu\n", ULLONG_MAX);

	printf("\n\n");
	return (0);
}